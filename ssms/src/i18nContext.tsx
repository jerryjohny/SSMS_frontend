type TranslationDictionary = (typeof translations)[Language];

interface I18nContextValue {
  language: Language;
  locale: string;
  copy: TranslationDictionary;
  setLanguage: (language: Language) => void;
}

function readStoredLanguage(): Language {
  try {
    const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return storedLanguage === "pt" ? "pt" : "en";
  } catch {
    return "en";
  }
}

function languageToLocale(language: Language): string {
  return language === "pt" ? "pt-MZ" : "en-MZ";
}

const I18nContext = createContext<I18nContextValue>({
  language: "en",
  locale: "en-MZ",
  copy: translations.en,
  setLanguage: () => {
    return;
  },
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => readStoredLanguage());

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language === "pt" ? "pt" : "en";
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      locale: languageToLocale(language),
      copy: translations[language],
      setLanguage,
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}
